export function getDriveById(id: string): Drive | undefined {
  return DRIVES.find((d) => d.id === id || d.slug === id);
}

export function getDrivesByIds(ids: DriveId[]): Drive[] {
  return ids
    .map((id) => DRIVES.find((d) => d.id === id))
    .filter((d): d is Drive => d !== undefined);
}

export const ALL_DRIVE_IDS: DriveId[] = DRIVES.map((d) => d.id);

export function getLatestUpdateDate(): string {
  return DRIVES.reduce(
    (latest, d) => (d.updatedAt > latest ? d.updatedAt : latest),
    DRIVES[0]?.updatedAt ?? ""
  );
}
